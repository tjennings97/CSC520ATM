	public void showPanel() {
		
		// Set this panel as the frame's content panel and update the frame title
		atmFrame.setContentPanel(this);
		atmFrame.setTitle(Main.ATM_FRAME_TITLE + " - (" + this.getClass().getSimpleName() + ")");
		
		// Actions prior to display
        
		// Default transaction type
		atmFrame.setTransactionType("TECHNICIAN");
		
		// Get the machine object to retrieve the statuses
		Machine machine = atmFrame.getMachine();
		
		// Set status values
		lblCashLevelStatus.setText(machine.getCashStatus());
		lblCashLevelStatus.setForeground(machine.getCashStatusColor());
		
		lblDepositLevelStatus.setText(machine.getDepositStatus());
		lblDepositLevelStatus.setForeground(machine.getDepositStatusColor());
		
		lblPrinterPaperLevelStatus.setText(machine.getPaperStatus());
		lblPrinterPaperLevelStatus.setForeground(machine.getPaperStatusColor());
		
		// Re-paint and re-validate to display the panel
		atmFrame.repaint();
		atmFrame.revalidate();

		// All done
		return;
	}
	
	/**
	 * Handles the actions that are requested by this panel
	 * 
	 * @param actionEvent An ActionEvent containing the details of the event
	 */
	@Override
	public void actionPerformed(ActionEvent actionEvent) {

		String action = actionEvent.getActionCommand();
		
		if(Main.DEBUG)
	    	System.err.println(this.getClass().getTypeName() + "."
			         + (new Throwable().getStackTrace()[0].getMethodName())
			         + ": Got action: : " + action
			          );
		
		switch (action) {
			
			case ACTION_RESET_CASH:
			{
				// Reset the cash amount
				resetCashAmount();
				
				// Update the screen
				showPanel();
				
				break;
			}
			
			case ACTION_RESET_DEPOSIT:
			{
				// Reset the deposit fill percent
				resetDepositFillPercent();
				
				// Update the screen
				showPanel();
				
				break;
			}
			
			case ACTION_RESET_PAPER:
			{
				// Reset the paper length
				resetPaperLength();
				
				// Update the screen
				showPanel();
				
				break;
			}
			
			case ACTION_PRINT:
			{
				// Print receipt
				
				// Check paper level
				if (atmFrame.getMachine().getPaperLength() > 0)
				{
					// Create the print receipt pane and place it on the frame
					JPanelPrintReceipt panelPrintReceipt = new JPanelPrintReceipt(atmFrame);
					panelPrintReceipt.showPanel();
				}
				else
				{
					lblMessage.setText("Out of paper.");
				}

				break;
			}
			
			case ACTION_DONE:
			{
				// Create the logon panel and place it on the frame
				JPanelLogon panelLogon = new JPanelLogon(atmFrame);
				panelLogon.showPanel();
				
				break;
			}
		}
		
		// All done
		return;
	}
	
